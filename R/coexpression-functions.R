#' @description
#' CLR transformation with package "compositions"
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#'
#' @return A matrix object of a transposed expression matrix where counts are 
#'    CLR transformed. The sample/gene orientation is the same as the input.
#'    
#' @importFrom compositions clr
#' @importFrom compositions clrInv
get_compositions <- function(counts) {
  run_clr <- compositions::clr(counts)
  clr_proportion <- compositions::clrInv(run_clr)
  return(clr_proportion)
}

#' @description
#' Pearson's correlation coefficient calculation with WGCNA::cor
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#'
#' @return A matrix object of a correlation matrix. Columns and rows are both
#'    gene names or locus tags, and maps to their pairwise correlation 
#'    coefficient.
#'    
#' @importFrom WGCNA cor
get_pcc <- function(counts) {
  cor_mat <- WGCNA::cor(counts, method = "pearson")
  return(cor_mat)
}

#' @description 
#' Proportionality calculation with package propr
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#'
#' @return A matrix object of a similarity matrix. Columns and rows are both
#'    gene names or locus tags, and maps to their pairwise proportionality. 
#'    
#' @importFrom propr propr
get_prop <- function(counts) {
  run_propr <- propr(counts,
                     metric = "rho", # rho as proportionality measure
                     ivar = "clr", # clr as selected log-ratio transformation 
                     alpha = NA,
                     p = 100)
  sim_matrix <- getMatrix(run_propr) # extract matrix from propr result
  # putting back matrix column and row names
  colnames(sim_matrix) <- colnames(counts) 
  rownames(sim_matrix) <- colnames(counts)
  return(sim_matrix)
}

#' @description
#' Extracts gene pairs and similarity measures from similarity matrix.
#' @note This function was generated by GPT-5.1.
#' 
#' @param sim_matrix A matrix object of a similarity or correlation matrix. 
#'    Columns and rows are both gene names or locus tags. 
#'
#' @return A data frame of all gene pairs and their similarities with 3 columns:
#'    - gene 1: gene name or locus tag of gene 1 of gene pair
#'    - gene 2: gene name or locus tag of gene 2 of gene pair
#'    - correlation: similarity measure of gene pair
get_genepairs <- function(sim_matrix) {
  # Get lower triangle indices (exclude diagonal) so each gene pair is only
  # extracted once
  idx <- which(lower.tri(sim_matrix, diag = FALSE), arr.ind = TRUE)
  
  # Build data frame of gene pairs and correlations
  corr_df <- data.frame(
    gene1 = rownames(sim_matrix)[idx[, 1]],
    gene2 = colnames(sim_matrix)[idx[, 2]],
    correlation = sim_matrix[idx]
  )
  
  # Sort by descending correlation
  corr_df <- corr_df[order(-corr_df$correlation), ]
  
  return(corr_df)
}

