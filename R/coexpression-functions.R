#' @description
#' Sort genes by mean expression
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#'
#' @return A matrix object of a transposed expression matrix, genes (columns) 
#'    sorted by mean gene expression. The sample/gene orientation is the same as
#'    the input.
sort_expression <- function(counts) {
  dataset_sorted <- counts[, names(sort(colMeans(counts)))]
  return(dataset_sorted)
}

