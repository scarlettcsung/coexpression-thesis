#' @description
#' Performs the entire "Original" workflow.
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#' @param replicates number of replicates per sample in data set
#'
#' @return A matrix object of a correlation matrix. Columns and rows are both
#'    gene names or locus tags, and maps to their pairwise correlation 
#'    coefficient.
#'    
#' @importFrom COBAIN logTMM
#' @importFrom COBAIN corAndSpQN
corrs_Original <- function(counts,replicates) {
  # Perform TMM, log transformation, and sort  by mean expression
  tmm <- logTMM(counts,replicates = replicates)
  sorted <- tmm[order(rowMeans(tmm)), ]
  # PCC + SpQN
  means <- stats::setNames(rowMeans(sorted), rownames(sorted))
  corr <- corAndSpQN(sorted,means)
  # Adding back gene names/locus tags
  corr <- corr[rownames(counts), rownames(counts), drop = FALSE]
  message(sprintf("Correlation matrix produced for method 'Original'"))
  return(corr)
}

#' @description
#' Performs the entire "Mean Residuals" workflow.
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#' @param replicates number of replicates per sample in data set
#'
#' @return A matrix object of a correlation matrix. Columns and rows are both
#'    gene names or locus tags, and maps to their pairwise correlation 
#'    coefficient.
#'    
#' @importFrom COBAIN logTMM
#' @importFrom COBAIN meanResiduals
#' @importFrom COBAIN corAndSpQN
corrs_MeanResiduals <- function(counts,replicates) {
  # Perform TMM, log transformation, and sort  by mean expression
  tmm <- logTMM(counts,replicates = replicates)
  sorted <- tmm[order(rowMeans(tmm)), ]
  # Mean Residuals Transformation
  lemmed <- meanResiduals(sorted)
  mr <- lemmed[rownames(sorted), ]
  # PCC + SpQN
  means <- stats::setNames(rowMeans(sorted), rownames(sorted))
  corr <- corAndSpQN(mr,means)
  # Adding back gene names/locus tags
  corr <- corr[rownames(counts), rownames(counts), drop = FALSE]
  message(sprintf("Correlation matrix produced for method 'MeanResiduals'"))
  return(corr)
}

