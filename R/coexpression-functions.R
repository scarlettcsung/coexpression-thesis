#' @description
#' Single function that performs all co-expression analysis workflows and 
#' produces the similarity matrices and extracts the sorted gene pairs.
#' 
#' @param counts A matrix object of a transposed expression matrix, such that
#'    Rows = Conditions or samples
#'    Columns = Genes or locus tags
#' @param replicates number of replicates per sample in data set
#'
#' @return A list of lists, containing matrices and gene pairs. 
#'    matrix: A list of matrices, containing similarity matrices produced by 
#'            each method. Row and column names are genes/locus tags.
#'    genepairs: A list of data frames produced by each method, containing gene 
#'            pairs and their similarities with 3 columns:
#'            - gene 1: gene name or locus tag of gene 1 of gene pair
#'            - gene 2: gene name or locus tag of gene 2 of gene pair
#'            - correlation: similarity measure of gene pair
#'    
#' @importFrom zCompositions cmultRepl
coexpr_analysis <- function(counts, replicates) {
  # Basic processing
  counts_t <- t(counts) # Transpose
  counts_sorted <- sort_expression(counts_t) # Sorting genes by mean expression
  # zCompositions zero imputation
  z_counts <- cmultRepl(counts_sorted,method = "CZM",output="p-counts") 
  
  # CLR
  comp <- get_compositions(z_counts)
  comp_pcc <- get_pcc(comp)
  message(sprintf("Correlation matrix produced for method 'CLR'"))
  # propr methods
  pr <- suppressMessages(get_prop(counts_sorted))
  message(sprintf("Similarity matrix produced for method 'propr'"))
  pr_z <- suppressMessages(get_prop(z_counts))
  message(sprintf("Similarity matrix produced for method 'propr + zCompositions'"))
  
  # TMM and log-transformation, and sorting by mean expression
  tmm <- logTMM(counts,replicates = replicates)
  sorted <- tmm[order(rowMeans(tmm)), ]
  # Mean Residuals transformation
  lemmed <- meanResiduals(sorted) 
  # PC1 Residuals transformation
  less_pc <- suppressMessages(removePCs(sorted, 1))
  
  # Combining results of COBAIN/COCOLOCUS methods to return row name labels 
  # together. Logic was retained from checkCorBias.ChatGPT was used here to 
  # understand and apply data processing. 
  results <- list(Original = sorted, 
                  MeanResiduals = lemmed, 
                  PC1Residuals = less_pc)
  results <- lapply(results, function(x) x[rownames(counts), ])
  
  # Calculating PCC and SpQN for COCOLOCUS/COBAIN methods
  means <- stats::setNames(rowMeans(sorted), rownames(sorted))
  corr_og <- corAndSpQN(results$Original,means)
  message(sprintf("Correlation matrix produced for method 'Original'"))
  corr_mr <- corAndSpQN(results$MeanResiduals, means)
  message(sprintf("Correlation matrix produced for method 'MeanResiduals'"))
  corr_pc1 <- corAndSpQN(results$PC1Residuals, means)
  message(sprintf("Correlation matrix produced for method 'PC1Residuals'"))
  
  # Compiling outputs
  # All matrices
  matrix <- list()
  matrix$Original = corr_og
  matrix$MeanResiduals = corr_mr
  matrix$PC1Residuals = corr_pc1
  matrix$CLR = comp_pcc
  matrix$propr = pr
  matrix$propr_z = pr_z
  
  # All gene pairs
  genepairs <- list()
  genepairs$Original = get_genepairs(corr_og)
  genepairs$MeanResiduals = get_genepairs(corr_mr)
  genepairs$PC1Residuals = get_genepairs(corr_pc1)
  genepairs$CLR = get_genepairs(comp_pcc)
  genepairs$propr = get_genepairs(pr)
  genepairs$propr_z = get_genepairs(pr_z)
  
  output <- list(matrix = matrix, genepairs = genepairs)
  
  return(output)
}

